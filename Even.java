// Program to display all even numbers from 1 to 50.
public class Even {
 
        public static void main(String[] args) {
               
                
                int l= 50;
               
                System.out.println("Printing Even numbers between 1 and 50");
               
                for(int i=1; i <= l; i++){
                       
                    
                        if( i % 2 == 0){
                                System.out.print(i + " ");
                        }
                }
        }
}
